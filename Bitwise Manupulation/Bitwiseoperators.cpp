// bitwise & AND
// bitwise | OR
// bitwise ^ XOR
//bitwise >> RIGHTSIFT
//bitwise << LEFTSHIFT

#include <iostream>
using namespace std;

int main() {
    int a = 4, b = 8;
    cout << (a | b) << endl;
    cout << (a ^ b) << endl;
    cout << (a & b) << endl; 
    cout << (4 << 1) << endl;
    cout << (10 << 2) << endl;
    cout << (10 >> 1) << endl;
    cout << (8 >> 2) << endl;
    cout << (5-2*6) << endl;
    return 0;
}